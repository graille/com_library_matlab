function mex_nonuniform_quantization_compiler()
    current_folder = fileparts(which(mfilename('fullpath')));
    
    mex_compiler( ...
        current_folder, ...
        'mex_nonuniform_quantization', ...
        { ...
            fullfile(current_folder, '..', '..', 'quantizer.c')
        }, ...
        {'-R2018a'} ...
    );
end